				vertices[7][0] = x;	vertices[7][1] = y+1;	vertices[7][2] = z+1;
	
				values[0] = grid[x][y][z];
				values[1] = grid[x+1][y][z];
				values[2] = grid[x+1][y+1][z];
				values[3] = grid[x][y+1][z];
				values[4] = grid[x][y][z+1];
